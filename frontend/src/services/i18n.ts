export function setLocale(locale: Locale) {
  localeState.value = locale
  localStorage.setItem(STORAGE_KEY, locale)
}

export function useI18n() {
  const locale = computed(() => localeState.value)
  const t = (key: keyof typeof messages.en) => messages[localeState.value][key]

  const roleLabel = (role?: string | null) => {
    if (role === 'Admin') return t('roleAdmin')
    if (role === 'PM') return t('rolePM')
    if (role === 'Contractor') return t('roleContractor')
    return role ?? ''
  }

  const projectStatusLabel = (status?: string | null) => {
    if (status === 'Planning') return t('planning')
    if (status === 'Active') return t('active')
    if (status === 'OnHold') return t('onHold')
    if (status === 'Completed') return t('completed')
    return status ?? ''
  }

  const variationStatusLabel = (status?: string | null) => {
    if (status === 'Draft') return t('draft')
    if (status === 'Submitted') return t('submitted')
    if (status === 'Approved') return t('approved')
    if (status === 'Rejected') return t('rejected')
    if (status === 'NeedInfo') return t('needInfo')
    return status ?? ''
  }

  return {
    locale,
    t,
    setLocale,
    roleLabel,
    projectStatusLabel,
    variationStatusLabel,
  }
}
